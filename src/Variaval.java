public class Variaval {
    public static void main(String[] args) {
        int idade = 25;
        double altura = 1.68;
        char sexo = 'F';
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Sexo: " + sexo);
    }
    /*
    Sintaxe:
    <tipo> <nome> = <valor inicial>;
    Uma variável possui:
    - Nome (ou identificador)
    - Tipo
    - Valor
    - Endereço
    */

}
